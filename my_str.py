import string

all_digit = string.digits
print(all_digit)
print(len(all_digit))

lowercase = string.ascii_lowercase
print(lowercase)
print(len(lowercase))

uppercase = string.ascii_uppercase
print(uppercase)
print(len(uppercase))

letter = string.ascii_letters
print(letter)
print(len(letter))

hex = string.hexdigits
print(hex)
print(len(hex))

oct = string.octdigits
print(oct)
print(len(oct))

punch = string.punctuation
print(punch)
print(len(punch))

table = string.printable
print(table)
print(len(table))






